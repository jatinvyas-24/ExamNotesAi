import PDFDocument from "pdfkit";

export const pdfDownload = async (req, res) => {
  try {
    const { result } = req.body;

    if (!result) {
      return res.status(400).json({ error: "No content provided" });
    }

    const doc = new PDFDocument({ margin: 50 });

    res.setHeader("Content-Type", "application/pdf");
    res.setHeader(
      "Content-Disposition",
      'attachment; filename="ExamNotesAI.pdf"'
    );

    doc.pipe(res);

    // ⭐ Convert +P → ⭐
    const formatStars = (text = "") => text.replace(/\+P/g, "k");

    // 🔹 Heading helper
    const heading = (text) => {
      doc
        .font("Helvetica-Bold")
        .fontSize(16)
        .fillColor("#111827")
        .text(text, { underline: true });

      doc.moveDown(0.7);
    };

    // 🔹 Bullet helper
    const bullet = (text) => {
      doc
        .font("Helvetica")
        .fontSize(12)
        .fillColor("black")
        .text(`• ${text}`, { lineGap: 4 });

      doc.moveDown(0.4);
    };

    // 🔹 Bold markdown renderer (**text**)
    const renderLine = (line) => {
      const parts = line.split("**");

      parts.forEach((part, i) => {
        doc
          .font(i % 2 === 1 ? "Helvetica-Bold" : "Helvetica")
          .fontSize(12)
          .text(part, {
            continued: i !== parts.length - 1,
          });
      });

      doc.text("");
    };

    // 🎯 Title
    doc
      .font("Helvetica-Bold")
      .fontSize(22)
      .fillColor("#4f46e5")
      .text("ExamNotes AI", { align: "center" });

    doc.moveDown();

    doc
      .font("Helvetica")
      .fontSize(14)
      .fillColor("black")
      .text(`Importance: ${formatStars(result.importance)}`);

    doc.moveDown(1);

    // 📚 Sub Topics
    heading("Sub Topics");

    Object.entries(result.subTopics || {}).forEach(([star, topics]) => {
      doc
        .font("Helvetica-Bold")
        .fontSize(13)
        .text(`${formatStars(star)} Topics:`);

      doc.moveDown(0.4);

      (topics || []).forEach(bullet);

      doc.moveDown(0.5);
    });

    // 📝 Notes
    heading("Notes");

    const lines = (result.notes || "")
      .replace(/\r\n/g, "\n")
      .replace(/\n{2,}/g, "\n\n")
      .split("\n");

    lines.forEach((line) => {
      if (!line.trim()) {
        doc.moveDown(0.5);
        return;
      }

      // Markdown heading (#)
      if (line.startsWith("#")) {
        doc
          .font("Helvetica-Bold")
          .fontSize(14)
          .text(line.replace(/#+\s/g, ""));

        doc.moveDown(0.4);
      }

      // Markdown bold (**text**)
      else if (line.includes("**")) {
        renderLine(line);
        doc.moveDown(0.3);
      }

      // Normal text
      else {
        doc
          .font("Helvetica")
          .fontSize(12)
          .text(line, { lineGap: 4 });

        doc.moveDown(0.3);
      }
    });

    doc.moveDown(1);

    // 🔁 Revision Points
    heading("Revision Points");

    (result.revisionPoints || []).forEach(bullet);

    doc.moveDown(1);

    // ❓ Important Questions
    heading("Important Questions");

    // Short Questions
    doc.font("Helvetica-Bold").fontSize(13).text("Short Questions:");
    doc.moveDown(0.4);

    (result.questions?.short || []).forEach(bullet);

    doc.moveDown(0.7);

    // Long Questions
    doc.font("Helvetica-Bold").fontSize(13).text("Long Questions:");
    doc.moveDown(0.4);

    (result.questions?.long || []).forEach(bullet);

    doc.moveDown(0.7);

    // Diagram Question
    doc.font("Helvetica-Bold").fontSize(13).text("Diagram Question:");
    doc.moveDown(0.4);

    bullet(result.questions?.diagram || "");

    // 📊 Diagram Image
    if (result.diagramImage) {
      doc.addPage();
      heading("Diagram");

      doc.image(result.diagramImage, {
        fit: [500, 300],
        align: "center",
      });

      doc.moveDown();
      doc.text("Figure: Diagram", { align: "center" });
    }

    // 📈 Chart Image
    if (result.chartImage) {
      doc.addPage();
      heading("Chart");

      doc.image(result.chartImage, {
        fit: [500, 300],
        align: "center",
      });

      doc.moveDown();
      doc.text("Figure: Chart", { align: "center" });
    }

    // 📌 Footer
    doc.moveDown(2);
    doc
      .font("Helvetica-Oblique")
      .fontSize(10)
      .fillColor("gray")
      .text("Generated by ExamNotes AI", { align: "center" });

    doc.end();
  } catch (error) {
    console.error("PDF Error:", error);
    res.status(500).json({ error: "Failed to generate PDF" });
  }
};